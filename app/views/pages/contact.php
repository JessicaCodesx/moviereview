// Royal FAQ toggle functionality
function toggleRoyalFAQ(element) {
    const faqItem = element.parentElement;
    const isActive = faqItem.classList.contains('active');

    // Close all other FAQ items
    document.querySelectorAll('.royal-faq-item').forEach(item => {
        item.classList.remove('active');
    });

    // Toggle current item
    if (!isActive) {
        faqItem.classList.add('active');
    }
}

// Enhanced card hover effects
document.querySelectorAll('.connect-card').forEach(card => {
    card.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-10px) scale(1.02)';
    });

    card.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(0) scale(1)';
    });
});

// Email contact functionality
document.querySelector('.email-card').addEventListener('click', function(e) {
    e.preventDefault();

    // Create professional email
    const subject = encodeURIComponent('Elite Cinema Project Inquiry');
    const body = encodeURIComponent(`Hello Jessica,

I came across your Elite Cinema project and would love to discuss:

[ ] Collaboration opportunities
[ ] Technical questions about the project
[ ] Professional opportunities
[ ] Other: 

Best regards,`);

    const mailtoLink = `mailto:jessicagarcia.codes@gmail.com?subject=${subject}&body=${body}`;
    window.location.href = mailtoLink;
});

// Initialize floating particles
function initializeFloatingParticles() {
    const particles = document.querySelectorAll('.particle');
    particles.forEach((particle, index) => {
        particle.style.animationDelay = `${index * 2.5}s`;
        particle.style.left = `${Math.random() * 100}%`;
        particle.style.top = `${Math.random() * 100}%`;
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    initializeFloatingParticles();

    // Add scroll animations
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    });

    document.querySelectorAll('.developer-intro-card, .project-showcase-section, .connect-section, .project-faq-section').forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(30px)';
        el.style.transition = 'all 0.6s ease';
        observer.observe(el);
    });
});
</script>

